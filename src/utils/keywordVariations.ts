// Generate page-specific keywords
export const generatePageKeywords = (pageType: string, location?: string) => {
  const baseKeywords = [
    ...businessNameVariations,
    ...serviceKeywordVariations
  ];
  
  if (location) {
    baseKeywords.push(...locationKeywordVariations.filter(loc => 
      loc.toLowerCase().includes(location.toLowerCase())
    ));
  }
  
  // Add page-specific keywords
  switch (pageType) {
    case 'home':
      return [
        ...baseKeywords,
        "digital services platform",
        "comprehensive services",
        "all in one services",
        "complete service solution"
      ].join(", ");
      
    case 'travel':
      return [
        ...baseKeywords,
        "flight booking",
        "bus booking", 
        "train booking",
        "rail booking",
        "ticket booking",
        "travel assistance",
        "travel help",
        "travel support"
      ].join(", ");
      
    case 'g2c':
      return [
        ...baseKeywords,
        "government services",
        "citizen services",
        "aadhaar services",
        "pan card services",
        "pmjay services",
        "government assistance",
        "citizen help"
      ].join(", ");
      
    case 'b2c':
      return [
        ...baseKeywords,
        "business services",
        "dsc services",
        "digital signature",
        "mobile recharge",
        "business assistance",
        "business help",
        "corporate services"
      ].join(", ");
      
    default:
      return baseKeywords.join(", ");
  }
};
